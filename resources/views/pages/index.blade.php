<h4 style="color:#BAD405"> Dear APPNA Oklahoma Members:</h4>



<p>I am very honored to be serving as the President of the APPNA Oklahoma Chapter for the term 2018-2019. We belong to one of the most vibrant APPNA Chapters in the country. Under the leadership of Dr. Iftikhar Hussain (APPNA OK Chapter President 2016-
2017), our APPNA Chapter made exceptional progress. I look forward to working with you and members of our executive committee, Dr. Iftikhar Hussain (Immediate Past President), Dr. Yasmin Sarfraz (Treasurer) and Dr. Faisal Latif (Secretary), to continue to carry APPNA's mission forward.  </p>


<br>
<p>Below are a series of goals for this term: </p>
<ul>
<li><strong>Financial transparency: </strong>Our Chapter&rsquo;s financial records were reconciled earlier this year and a financial report was submitted to National APPNA for the purpose of re-certification of this Chapter. It is one of the goals of this Executive Committee to keep financial records of this chapter completely transparent to its members.</li>
<li><strong>Streamlining our payment process: </strong>Another goal is to facilitate easier and more secure payments for membership dues, charitable donations to Chapter causes, and purchasing of tickets for Chapter&rsquo;s programs. In order to make the transaction process more convenient for members, our Chapter&rsquo;s website is being rebuilt to add a payment gateway using PayPal.</li>
<li><strong>Improving communication: </strong>We will make every possible effort to enhance communication with members to inform about our Chapter&rsquo;s and National APPNA&rsquo;s activities and initiatives. To achieve this goal, we will be updating our Chapter&rsquo;s blast email system and rebuilding our Chapter&rsquo;s website.</li>
<li><strong>Social media: </strong>Members will have the opportunity to connect through social media platforms such as Facebook and through our Chapter&rsquo;s WhatsApp chat group. New members will be added to the WhatsApp chat group on a regular basis.</li>
<li><strong>Community engagement activities:</strong> Our Chapter will organize health fairs for the community members by mobilizing our Chapter&rsquo;s resources as well as by collaborating with National APPNA and other community organizations. Our Chapter plans to organize two health fairs to commemorate National APPNA&rsquo;s National Healthcare Day on October 13. Additionally, Chapter also plans to organize Heart Wellness Fairs.</li>
<li><strong>Chapter&rsquo;s Annual Meeting and Banquet: </strong>This event will be held in Oklahoma City in November 2018.Renowned physicians, along with their family and friends, are expected to attend this event. We are working hard to make the 2018 APPNA Oklahoma Chapter Annual Meeting and Banquet a rewarding and
memorable experience for all with Continuing Education Meetings followed by banquet and entertainment.</li>
<li><strong>Youth engagement: </strong>Another important goal is to involve young physicians in our Chapter&rsquo;s activities, and nurture them to take Chapter&rsquo;s leadership roles. <br /> </li>
</ul>
<p>I earnestly seek support from all the members of this chapter to run this chapter successfully, to implement above-mentioned goals, and to explore new avenues.</p>
